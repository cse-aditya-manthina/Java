class CompoundAssignmentOperation {
	public static void main(String[] args) {
		
	
	int i = 10;
	System.out.println("value of i is :"+i);
	i +=200;
	System.out.println("Value of i+=15 is :"+i);
	System.out.println("Value of i-=15 is :"+(i-=15));
	System.out.println("Value of i/=15 is :"+(i/=15));
	System.out.println("Value of i%=15 is :"+(i%=15));

}
}